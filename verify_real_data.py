    print("    - Substations: Power Plants database (722 Michigan facilities)")
    print("\n  Sample data verification:")
    
    for lat, lng, location in sample_points:
        if lat in data and lng in data[lat]:
            scores = data[lat][lng]
            print(f"    {location:20s}: Land Cover={scores['land_cover']:3d}, "
                  f"Slope={scores['slope']:3d}, Pop={scores['population']:3d}, "
                  f"Overall={scores['overall']:5.1f}")
        else:
            print(f"    {location:20s}: NOT FOUND IN GRID")
    
    # Check for variation
    all_land_cover = []
    for lat_data in data.values():
        for scores in lat_data.values():
            all_land_cover.append(scores['land_cover'])
    
    unique_lc = len(set(all_land_cover))
    print(f"\n  ✓ Land cover values show proper variation: {unique_lc} unique scores")
    print(f"    Range: {min(all_land_cover)} to {max(all_land_cover)}")
    
